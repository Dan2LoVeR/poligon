# Лемматизация
import pymorphy3
# Импортируем установленную библиотеку

morph = pymorphy3.MorphAnalyzer()
# Создаем объект морфологического анализатора

def lemmatize(allText):
    res = list()
    for word in allText:
        p = morph.parse(word)[0]
        # Анализируем слово и берем первый вариант разбора (наиболее вероятный)
        res.append(p.normal_form)
        # Добавляем нормальную форму слова (лемму) в результат
    return res

# Применяем лемматизацию
allText = lemmatize(allText)
# Преобразуем все слова в тексте к их начальной форме

# Удаляем повторы слов
unique_words = list(set(allText))
# Преобразуем список в множество (удаляет дубликаты), затем обратно в список

print(f"Уникальных слов после лемматизации: {len(unique_words)}")
# Выводим количество уникальных слов после обработки