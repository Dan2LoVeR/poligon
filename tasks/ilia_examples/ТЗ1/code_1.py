# Чтение файла
with open('tz1_data.txt', 'r', encoding='utf-8') as file:
    data = file.read()
# Открываем файл tz1_data.txt с диска в режиме чтения ('r')
# Указываем кодировку utf-8 для корректного чтения русских символов
# Содержимое файла сохраняем в переменную data

# Очистка данных
import re
# Импортируем модуль re для работы с регулярными выражениями

# Удаляем знаки препинания и небуквенные символы, приводим к нижнему регистру
allText = re.findall(r'\b[а-яё]+\b', data.lower())
# Используем регулярное выражение для поиска всех русских слов:
# \b - граница слова, [а-яё]+ - одна или более русских букв (включая ё)
# data.lower() - преобразуем весь текст к нижнему регистру
# Результат - список всех слов в тексте без знаков препинания

print(f"Всего слов в тексте: {len(allText)}")
print("Первые 20 слов:", allText[:20])
# Выводим информацию о количестве слов и первые 20 слов для проверки